CREATE TABLE IF NOT EXISTS `user_and_invoice` (
    `id` bigint NOT NULL AUTO_INCREMENT,
    `is_checked` bit(1) DEFAULT NULL,
    `optlock` int NOT NULL DEFAULT '0',
    `date_from_id` bigint DEFAULT NULL,
    `date_to_id` bigint DEFAULT NULL,
    `invoice_id` bigint DEFAULT NULL,
    `person_deliver_id` bigint DEFAULT NULL,
    `person_invoice_id` bigint DEFAULT NULL,
    PRIMARY KEY (`id`),
    KEY `idx_date_from_id` (`date_from_id`),
    KEY `idx_date_to_id` (`date_to_id`),
    KEY `idx_invoice_id` (`invoice_id`),
    KEY `idx_user_deliver_id` (`person_deliver_id`),
    KEY `idx_user_invoice_id` (`person_invoice_id`),
    CONSTRAINT `fk_ui_invoice` FOREIGN KEY (`invoice_id`) REFERENCES `invoice` (`id`),
    CONSTRAINT `fk_ui_date_to` FOREIGN KEY (`date_to_id`) REFERENCES `dates` (`id`),
    CONSTRAINT `fk_ui_date_from` FOREIGN KEY (`date_from_id`) REFERENCES `dates` (`id`),
    CONSTRAINT `fk_ui_user_deliver` FOREIGN KEY (`person_deliver_id`) REFERENCES `user` (`id`),
    CONSTRAINT `fk_ui_user_invoice` FOREIGN KEY (`person_invoice_id`) REFERENCES `user` (`id`)
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;
